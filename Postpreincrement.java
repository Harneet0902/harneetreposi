package stack_using_array;

public class Postpreincrement {
	
	 public static void main(String[] args) {
	        int i =0;
	        System.out.println(i++); //0
	        System.out.println(i); //1
	        i=0;
	        System.out.println(++i); //1
	        System.out.println(i); //1
	    }

}
